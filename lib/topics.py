def get_topic(time_slot: str) -> str:
    """日付とタイムスロットに基づいてトピックを選択する"""
    now = datetime.now(JST)
    day_of_year = now.timetuple().tm_yday
    year = now.year

    # 年と日を組み合わせてインデックスを生成（年が変わっても重複しにくい）
    seed = (year * 1000 + day_of_year)

    if time_slot == "morning":
        topics = MORNING_TOPICS
    elif time_slot == "noon":
        topics = NOON_TOPICS
    elif time_slot == "evening":
        topics = EVENING_TOPICS
    else:
        topics = MORNING_TOPICS

    index = seed % len(topics)
    return topics[index]
